    private void armarCabecera() {
        ArrayList<Object> filaCabecera = new ArrayList<>();
        filaCabecera.add("Codigo");
        filaCabecera.add("Cuit");
        filaCabecera.add("Nombre Laboratorio");
        filaCabecera.add("PaÃ­s");
        filaCabecera.add("Dominio Comercial");
        filaCabecera.add("Estado");
        for (Object it : filaCabecera) {
            modelo.addColumn(it);
        }
        jTLab.setModel(modelo);
    }

    private void llenarTabla() {
        listaLab = labD.listarLaboratorios();
        listaLab.stream().forEach(l -> {
            modelo.addRow(new Object[]{l.getIdLaboratorio(),l.getCuit(),l.getNomLaboratorio(),
                l.getPais(),l.getDomComercial(),l.isEstado()});
        });
    }

    private void actT() {
        modelo.setNumRows(0);
        llenarTabla();
        al.setAct(false);
    }
