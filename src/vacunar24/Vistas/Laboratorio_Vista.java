    private void jBModActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBModActionPerformed
        if (jTLab.getSelectedRow() >= 0) {
            lab = labD.buscarLaboratorioId((int) jTLab.getValueAt(jTLab.getSelectedRow(), 0));
            al.getjTCuit().setText(lab.getCuit()+"");
            al.getjTNomL().setText(lab.getNomLaboratorio());
            al.getjTPais().setText(lab.getPais());
            al.getjTDomC().setText(lab.getDomComercial());
            al.setIdMod(lab.getIdLaboratorio());
            al.setMod(true);
            al.show();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un laboratorio en la tabla...");
        }
    }//GEN-LAST:event_jBModActionPerformed

    private void jBElimActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBElimActionPerformed
        if (jTLab.getSelectedRow() >= 0) {
            labD.eliminarLaboratorio((int) jTLab.getValueAt(jTLab.getSelectedRow(), 0));
            actT();
        } else {
            JOptionPane.showMessageDialog(this, "Seleccione un laboratorio en la tabla...");
        }
    }//GEN-LAST:event_jBElimActionPerformed

    private void jPanel1MouseMoved(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel1MouseMoved
        if (al.isAct()) {
            actT();
        }
    }//GEN-LAST:event_jPanel1MouseMoved

    private void jTBuscadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTBuscadorKeyReleased
        modelo.setNumRows(0);
        for (Laboratorio l : listaLab) {
            if (l.getNomLaboratorio().toLowerCase().startsWith(jTBuscador.getText().toLowerCase()) || l.getCuit() == Integer.parseInt(jTBuscador.getText())
                    || l.getIdLaboratorio() == Integer.parseInt(jTBuscador.getText()) || l.getDomComercial().toLowerCase().startsWith(jTBuscador.getText().toLowerCase())) {
                modelo.addRow(new Object[]{l.getIdLaboratorio(),l.getCuit(),l.getNomLaboratorio(),l.getPais(),l.getDomComercial(),l.isEstado()});
            }
        }
    }//GEN-LAST:event_jTBuscadorKeyReleased

